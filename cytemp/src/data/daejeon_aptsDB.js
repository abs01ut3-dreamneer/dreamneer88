const DEFAULT_PYEONG_PATTERN = [22, 25, 28, 31, 34];

// 평수와 총가격(만원)을 추가한 최종 데이터
export const DAEJEON_APTS = BASE_DAEJEON_APTS.map((apt, idx) => {
  const areaPyeong =
    apt.areaPyeong ?? DEFAULT_PYEONG_PATTERN[idx % DEFAULT_PYEONG_PATTERN.length];
  const totalPrice = Math.round((apt.currentPrice ?? 0) * areaPyeong);

  return {
    ...apt,
    areaPyeong,
    areaSqm: Math.round(areaPyeong * 3.3),
    totalPrice, // 단위: 만원
  };
});
